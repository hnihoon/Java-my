package castingTest;

public class CastingTest2 {

	public static void main(String[] args) {
		char data1 = 66;
		char data2 = 67;
		char data3 = 68;
		
		
		System.out.println(data1);		//B
		System.out.println(data2);		//C
		System.out.println(data3);		//D
		
		System.out.println("=================================");
		
		System.out.println(data1 + 4);	//70
		
	}

}
